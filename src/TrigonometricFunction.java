
public class TrigonometricFunction {
	public static void main(String[] args) {
		System.out.println("90°的正弦值："+Math.sin(Math.PI/2));
		System.out.println("0°的正弦值："+Math.sin(0));
		
		System.out.println("60°的正切值："+Math.tan(Math.PI/3));
		
		System.out.println("2的平方根与2商的反弦值："+Math.asin(Math.sqrt(2)/2));
		System.out.println("1的反正切值："+Math.atan(1));
		
		System.out.println("120°的弧度值："+Math.toRadians(120.0));
		System.out.println("π/2的角度值："+Math.toDegrees(Math.PI/2));
	}

}
